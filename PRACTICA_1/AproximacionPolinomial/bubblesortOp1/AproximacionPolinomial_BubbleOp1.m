%%VALORES DE X 
x = [100, 1000, 5000, 10000, 15000, 50000, 80000, 100000, 150000, 200000, 250000, 275000, 300000, 320000, 375000, 400000, 500000, 600000, 800000, 1000000];

%%VALORES DE Y = F(x) en Milisegundos
%bubbleop1
y=[4.48227E-05 3.2470226288e-03 9.4790935516e-02 4.2672204971e-01 1.0256671906e+00 1.2506879091e+01 3.2566226959e+01 5.0953411818e+01 1.1493780708e+02 2.0408615208e+02 3.1915139294e+02 3.8710394287e+02 4.5942688584e+02 5.2278563404e+02 5.2278563404e+02 8.1694167614e+02 1.2752776399e+03 1.8380762289e+03 3.2703919089e+03 5.1112584860e+03];

%%OBTENER UN POLINOMIO QUE SE AJUSTE A LOS PUNTOS X Y Y
grado=6; 				%%GRADO DEL POLINOMIO DE AJUSTE
p=polyfit(x,y,grado)	%%FUNCION QUE REALIZA EL AJUSTE POLINOMIAL

%%SE CREA UN ESPACIO PARA REALIZAR UNA GRAFICA EN 
%%DONDE SE COMPARA LA CURVA AJUSTADA CONTRA LOS 
%%VALORES EXPERIMENTALES
xi=linspace(0,1000000);      %%ESPACIO DE PUNTOS PARA Xi 5000,
z=polyval(p,xi);				%%EVALUACION DEL POLINOMIO P EN EL ESPACIO Xi

%%SE REALIZA LA FIGURA CORRESPONDIENTE
figure(1);
plot(x,y,'o',xi,z,'-'),grid;
ylabel('F(x)');
xlabel('x');
title('Ajuste polinomial');